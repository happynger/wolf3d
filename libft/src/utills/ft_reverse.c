#include "libft.h"

void	ft_reverse(char *str)
{
	char	*s;
	char	*t;

	s = str;
	t = &str[ft_strlen(str) - 1];
	while (s < t)
	{
		SWAP(*s, *t);
		s++;
		t--;
	}
}
