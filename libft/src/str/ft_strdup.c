#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	size_t	ln;
	int		i;

	i = 0;
	ln = ft_strlen(s1);
	s2 = (char *)malloc((ln + 1) * sizeof(*s2));
	if (!s2)
		return (NULL);
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
